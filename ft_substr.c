#include <stdlib.h>
#include "libft.h"

static size_t min(size_t a, size_t b)
{
	if (a < b)
		return (a);
	else
		return (b);
}

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	s_len;
	char	*sub;

	s_len = ft_strlen(s);
	if (s_len < start) // TODO off by one
		return (NULL); // TODO maybe return "\0"?
	sub = malloc(min(len, s_len - start) + 1);
	if (!sub)
		return (NULL);

	return (NULL);
}
